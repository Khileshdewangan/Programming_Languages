#include <iostream>
using namespace std;
int main()
{
    int a = 55;
    int b = 8;

    //arthmatic oprator
    cout << "a + b =" << a + b << endl;
    cout << "a - b =" << a - b << endl;
    cout << "a * b =" << a * b << endl;
    cout << "a / b =" << a / b << endl;
    cout << "a % b =" << a % b << endl;
    cout << "a++ =" << a++ << endl;
    cout << "a-- =" << a-- << endl;
    cout << "++a =" << ++a << endl;
    cout << "--a =" << --a << endl;

    cout << "enter the value:\n";
    return 0;
}
